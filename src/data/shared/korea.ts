/** 전도(全圖) 지리 범위 — 제주(남)~압록강·두만강(북) */
export const KOREA_BBOX = {
  lngMin: 124.2,
  lngMax: 130.9,
  latMin: 33.05,
  latMax: 43.1,
};

/** 위도 38°가 해안선과 만나는 경도 범위 — 38선을 반도 폭에 맞춰 그릴 때 사용 */
export const LAT38_LNG: [number, number] = [125.12, 128.76];

export const seoul: LngLat = [126.98, 37.57];

/**
 * 전도용 좌표 매핑 — 표준위도 38°의 등장방형 근사(경도에 cos 38° 보정)로
 * 위·아래로 찌그러지지 않는 실루엣을 만들고, 주어진 뷰포트에 fit + 중앙 정렬한다.
 */
export function makeKoreaToXY(w: number, h: number, pad: number) {
  const kx = Math.cos((38 * Math.PI) / 180);
  const uw = (KOREA_BBOX.lngMax - KOREA_BBOX.lngMin) * kx;
  const uh = KOREA_BBOX.latMax - KOREA_BBOX.latMin;
  const s = Math.min((w - 2 * pad) / uw, (h - 2 * pad) / uh);
  const ox = (w - uw * s) / 2;
  const oy = (h - uh * s) / 2;
  return ([lng, lat]: LngLat): [number, number] => [
    ox + (lng - KOREA_BBOX.lngMin) * kx * s,
    oy + (KOREA_BBOX.latMax - lat) * s,
  ];
}
